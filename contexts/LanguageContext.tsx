const translations = {
  EN: enTranslations,
  PT: ptTranslations,
  FR: frTranslations,
  DE: deTranslations,
  LB: lbTranslations
};

interface LanguageContextProps {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: typeof enTranslations;
  tCategory: (catId: string) => string;
}

const LanguageContext = createContext<LanguageContextProps | undefined>(undefined);

export const LanguageProvider: React.FC<{ children: ReactNode }> = ({ children }) => {
  // SET DEFAULT LANGUAGE TO FRENCH ('FR') AS REQUESTED
  const [language, setLanguage] = useState<Language>('FR');

  const t = translations[language];

  // Helper to translate categories since they are dynamic IDs
  const tCategory = (catId: string) => {
    return catId; 
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t, tCategory }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
};
